const JMA_ICON_BASE = "https://www.jma.go.jp/bosai/forecast/img/";

/**
 * JMA 天気コードから昼アイコンの URL を返す。
 * TELOPS 表に無い未知コードは null（描画側で非表示にする）。
 */
export function jmaIconUrl(weatherCode: string | number | null | undefined): string | null {
  if (weatherCode == null) return null;
  const file = JMA_TELOPS_DAY_ICON[String(weatherCode)];
  return file ? JMA_ICON_BASE + file : null;
}
